// Command gen writes pkg/emoji/standard_emoji_gen.go: the built-in reaction
// shortcode allowlist, derived from kyokomi/emoji and filtered to the wire regex.
package main

import (
	"os"
	"regexp"
	"sort"
	"strings"
	"text/template"

	"github.com/kyokomi/emoji/v2"
)

var wire = regexp.MustCompile(`^[a-z0-9_+-]{1,32}$`)

var tmpl = template.Must(template.New("g").Parse(`// Code generated by pkg/emoji/gen; DO NOT EDIT.
package emoji

var standardEmoji = map[string]struct{}{
{{range .}}	{{printf "%q" .}}: {},
{{end}}}
`))

func main() {
	set := map[string]struct{}{}
	for k := range emoji.CodeMap() {
		name := strings.Trim(k, ":")
		if wire.MatchString(name) {
			set[name] = struct{}{}
		}
	}
	names := make([]string, 0, len(set))
	for n := range set {
		names = append(names, n)
	}
	sort.Strings(names)

	f, err := os.Create("../standard_emoji_gen.go")
	if err != nil {
		panic(err)
	}
	defer f.Close()
	if err := tmpl.Execute(f, names); err != nil {
		panic(err)
	}
}
